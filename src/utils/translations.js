// Función para obtener la traducción
export const t = (key, language = 'es') => {
  return translations[language]?.[key] || translations['es'][key] || key;
};

// Función para cambiar el idioma de toda la página
export const changeLanguage = (language) => {
  // Cambiar el atributo lang del HTML
  document.documentElement.lang = language;
  
  // Guardar en localStorage
  localStorage.setItem('selectedLanguage', language);
  
  // Disparar evento personalizado para notificar el cambio
  window.dispatchEvent(new CustomEvent('languageChanged', { detail: { language } }));
  
  // Traducir elementos dinámicamente
  translatePage(language);
};

// Función para traducir la página
export const translatePage = (language) => {
  const elements = document.querySelectorAll('[data-translate]');
  
  elements.forEach(element => {
    const key = element.getAttribute('data-translate');
    const translation = t(key, language);
    
    if (translation && translation !== key) {
      if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
        element.placeholder = translation;
      } else {
        element.textContent = translation;
      }
    }
  });
};

// Función para inicializar el sistema de traducciones
export const initTranslations = () => {
  const savedLanguage = localStorage.getItem('selectedLanguage') || 'es';
  changeLanguage(savedLanguage);
  
  // Escuchar cambios de idioma
  window.addEventListener('languageChanged', (event) => {
    translatePage(event.detail.language);
  });
};
